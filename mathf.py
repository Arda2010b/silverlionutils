def add(x, y, ans):
    ans = x + y
    return(ans)

def subtract(x, y, ans):
    ans = x - y
    return(ans)

def multiply(x, y, ans):
    ans = x * y
    return(ans)

def divide(x, y, ans):
    ans = x /y
    return(ans)

def squared(x, ans):
    ans = x * x
    return(ans)

def cubed(x, ans):
    ans = x * x * x
    return(ans)